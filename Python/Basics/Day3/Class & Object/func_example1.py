"""
Functions for adding and multiply two numbers.
"""

class Calc:
    def add(self,a, b):
        c = a + b
        print(c)

    def multi(self,a, b):
        c = a * b
        print(c)

    def sub(self,a, b):
        c = a - b
        print(c)

    def div(self,a, b):
        c = a / b
        print(c)

